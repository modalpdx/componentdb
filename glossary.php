    <h1>Glossary</h1>

    <dl>
        <dt>Component</dt>
        <dd>Any discrete element of an electronics hardware circuit.
        Components include items such as resistors, microprocessors, and
        capacitors, as well as other items that would be snapped or
        soldered together to form an electronic device.</dd>

        <dt>Measure</dt>
        <dd>A rating of a particular value of a component. Common measures
        include ohms (for resistors), uF (micro-farads, for capacitors),
        watts, and A (amps).</dd>

        <dt>Value</dt>
        <dd>The numeric "level" of the measure of the component. Example:
        if the Measure is volts, common voltage Values are 5 and 3.3.</dd>

        <dt>Package</dt>
        <dd>The mounting form-factor of the component. Common packages
        include TSSOP (thin-shrink small outline package), DIP (dual inline 
        pins), and QFN (quad flat, no-leads).</dd>

